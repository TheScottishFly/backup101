int		ft_fint_prime(int nb)
{
	int i;
	int j;

	i = 2;
	j = 0;

	if (nb == 0 || nb == 1)
		return (0);
	while(i < nb)
	{
		if (nb % i == 0)
		{
			j = i;
		}
		i++;
	}
	if (j != 0)
		return (0);
	return (1);
}
