int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb <= 0)
		return (0);
	while (i * i < nb)
		if (i + 1 * i + 1 < nb)
			i++;
		else
			break ;
	if (nb % i == 0)
		return (i);
	else
		return (0);
}
